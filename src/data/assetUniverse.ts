export const ALL_ASSETS: AssetSearchItem[] = dedupeAssets([
  ...BIST_ASSETS,
  ...US_ASSETS,
  ...FX_ASSETS,
  ...METAL_ASSETS,
  ...FUND_ASSETS,
]);

function normalizeText(value: string): string {
  return value
    .toLowerCase()
    .replace(/ı/g, 'i')
    .replace(/ğ/g, 'g')
    .replace(/ü/g, 'u')
    .replace(/ş/g, 's')
    .replace(/ö/g, 'o')
    .replace(/ç/g, 'c')
    .trim();
}

export function dedupeAssets(items: AssetSearchItem[]): AssetSearchItem[] {
  const map = new Map<string, AssetSearchItem>();
  items.forEach(item => {
    const symbol = item.symbol.trim().toUpperCase();
    const key = `${item.market}:${symbol}`;
    if (!map.has(key)) {
      map.set(key, { ...item, symbol });
    }
  });
  return Array.from(map.values()).sort((a, b) => {
    const marketOrder = ['BIST', 'NASDAQ', 'NYSE', 'FUND', 'FX', 'METAL'];
    const ma = marketOrder.indexOf(a.market);
    const mb = marketOrder.indexOf(b.market);
    if (ma !== mb) return ma - mb;
    return a.symbol.localeCompare(b.symbol, 'tr');
  });
}

export function filterByMarket(items: AssetSearchItem[], market: 'ALL' | AssetMarket): AssetSearchItem[] {
  if (market === 'ALL') return items;
  if (market === 'NASDAQ') return items.filter(item => item.market === 'NASDAQ');
  if (market === 'NYSE') return items.filter(item => item.market === 'NYSE');
  return items.filter(item => item.market === market);
}

// Search function for autocomplete and market discovery.
export function searchAssets(query: string, limit: number = 20): AssetSearchItem[] {
  const max = Math.max(1, limit);
  if (!query || query.trim().length < 1) return ALL_ASSETS.slice(0, max);

  const q = normalizeText(query);
  const scored = ALL_ASSETS.map(asset => {
    let score = 0;
    const sym = normalizeText(asset.symbol);
    const nm = normalizeText(asset.name);

    if (sym === q) score += 120;
    else if (sym.startsWith(q)) score += 80;
    else if (sym.includes(q)) score += 45;

    if (nm === q) score += 90;
    else if (nm.startsWith(q)) score += 55;
    else if (nm.includes(q)) score += 30;

    if (asset.market === 'NASDAQ' || asset.market === 'NYSE') score += 3;
    if (asset.market === 'BIST') score += 2;

    return { asset, score };
  });

  return scored
    .filter(item => item.score > 0)
    .sort((a, b) => b.score - a.score || a.asset.symbol.localeCompare(b.asset.symbol, 'tr'))
    .slice(0, max)
    .map(item => item.asset);
}

export async function searchAssetsRemote(query: string, market: 'ALL' | AssetMarket = 'ALL', limit: number = 30): Promise<AssetSearchItem[]> {
  if (!query || query.trim().length < 1) return [];
  try {
    const params = new URLSearchParams({ q: query.trim(), market, limit: String(limit) });
    const res = await fetch(`/api/search?${params.toString()}`);
    if (!res.ok) return [];
    const data = await res.json();
    const rows = Array.isArray(data?.results) ? data.results : Array.isArray(data) ? data : [];
    return dedupeAssets(rows.filter((item: Partial<AssetSearchItem>) =>
      item && item.symbol && item.name && item.market && item.type && item.currency
    ) as AssetSearchItem[]);
  } catch (err) {
    console.warn('Remote asset search failed:', err);
    return [];
  }
}

export function mergeAssetResults(...groups: AssetSearchItem[][]): AssetSearchItem[] {
  return dedupeAssets(groups.flat());
}

export function getYahooSymbol(asset: AssetSearchItem): string {
  if (asset.market === 'BIST') return `${asset.symbol.replace(/\.IS$/i, '')}.IS`;
  return asset.symbol;
}

